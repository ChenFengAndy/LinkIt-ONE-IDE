#ifndef _LBATTERY_H
#define _LBATTERY_H

#include "LTask.h"


// LBattery class interface, With this library, you can get battery infomation like charging status and battery level
class LBatteryClass  : public _LTaskClass {

// Constructor / Destructor
public:
	LBatteryClass() { };

// Method
public:
	// DESCRIPTION
	//     Get the battery level of the board.
	// RETURNS
	//     battery level, return value maybe one of 0,33,66,100
	int level();
	
	// DESCRIPTION
	//     Charging or not.
	// RETURNS
	//     true means current is charging, false means not charging
	boolean isCharging();

private:
	int m_level;
	boolean m_charging;
};

extern LBatteryClass LBattery;

#endif
